package com.dajung.dajungworld.dto;

public class WorldFamilyDTO {

	private int family_num;
	private String family_reply;
	private int member_num;
	private String family_date;
	
	public int getFamily_num() {
		return family_num;
	}
	public void setFamily_num(int family_num) {
		this.family_num = family_num;
	}
	public String getFamily_reply() {
		return family_reply;
	}
	public void setFamily_reply(String family_reply) {
		this.family_reply = family_reply;
	}
	public int getMember_num() {
		return member_num;
	}
	public void setMember_num(int member_num) {
		this.member_num = member_num;
	}
	public String getFamily_date() {
		return family_date;
	}
	public void setFamily_date(String family_date) {
		this.family_date = family_date;
	}
	
	
}
