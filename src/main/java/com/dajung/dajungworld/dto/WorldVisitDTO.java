package com.dajung.dajungworld.dto;

public class WorldVisitDTO {

	private int visit_num;
	private String visit_content;
	private String visit_date;
	private int member_num;
	private int main_num;
	
	public int getVisit_num() {
		return visit_num;
	}
	public void setVisit_num(int visit_num) {
		this.visit_num = visit_num;
	}
	public String getVisit_content() {
		return visit_content;
	}
	public void setVisit_content(String visit_content) {
		this.visit_content = visit_content;
	}
	public String getVisit_date() {
		return visit_date;
	}
	public void setVisit_date(String visit_date) {
		this.visit_date = visit_date;
	}
	public int getMember_num() {
		return member_num;
	}
	public void setMember_num(int member_num) {
		this.member_num = member_num;
	}
	public int getMain_num() {
		return main_num;
	}
	public void setMain_num(int main_num) {
		this.main_num = main_num;
	}
	
	
	
	
	
	
	
	
}
